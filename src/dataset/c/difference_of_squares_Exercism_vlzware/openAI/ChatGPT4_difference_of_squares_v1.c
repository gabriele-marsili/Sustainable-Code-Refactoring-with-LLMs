#include "difference_of_squares.h"

int square_of_sum(const int n)
{
    return (n * (n + 1) / 2) * (n * (n + 1) / 2);
}

int sum_of_squares(const int n)
{
    return n * (n + 1) * (2 * n + 1) / 6;
}

int difference_of_squares(const int n)
{
    int sum = n * (n + 1) / 2;
    return (sum * sum) - (n * (n + 1) * (2 * n + 1) / 6);
}