package brackets

func Bracket(input string) bool {
	stack := make([]rune, 0, len(input)) // Initialize stack with capacity
	for _, c := range input {
		switch c {
		case '[', '(', '{':
			stack = append(stack, c)
		case ']':
			if len(stack) == 0 || stack[len(stack)-1] != '[' {
				return false
			}
			stack = stack[:len(stack)-1]
		case ')':
			if len(stack) == 0 || stack[len(stack)-1] != '(' {
				return false
			}
			stack = stack[:len(stack)-1]
		case '}':
			if len(stack) == 0 || stack[len(stack)-1] != '{' {
				return false
			}
			stack = stack[:len(stack)-1]
		}
	}
	return len(stack) == 0
}