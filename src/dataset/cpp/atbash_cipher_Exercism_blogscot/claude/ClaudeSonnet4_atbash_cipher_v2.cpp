string encode(const string& text) {
    string result;
    result.reserve(text.length() + text.length() / 4);
    
    int letterCount = 0;
    for (unsigned char c : text) {
        char mapped = cipher_map[c];
        if (mapped) {
            result += mapped;
            if (++letterCount % 5 == 0) {
                result += ' ';
            }
        }
    }
    
    if (!result.empty() && result.back() == ' ') {
        result.pop_back();
    }
    
    return result;
}

string decode(const string& text) {
    string result;
    result.reserve(text.length());
    
    for (unsigned char c : text) {
        if (c != ' ') {
            result += reverse_map[c];
        }
    }
    
    return result;
}

