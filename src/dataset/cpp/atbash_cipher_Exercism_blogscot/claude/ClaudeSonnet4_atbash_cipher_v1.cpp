string encode(const string& text) {
    string result;
    result.reserve(text.length() + text.length() / 4); // Pre-allocate with space estimate
    
    int letterCount = 0;
    
    for (unsigned char c : text) {
        char encoded = encode_table[c];
        if (encoded != 0) {
            result += encoded;
            if (++letterCount % 5 == 0) {
                result += ' ';
            }
        }
    }
    
    // Remove trailing space if present
    if (!result.empty() && result.back() == ' ') {
        result.pop_back();
    }
    
    return result;
}

string decode(const string& text) {
    string result;
    result.reserve(text.length()); // Pre-allocate
    
    for (unsigned char c : text) {
        if (!std::isspace(c)) {
            result += decode_table[c];
        }
    }
    
    return result;
}

